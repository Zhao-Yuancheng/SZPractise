                  </p>
                  <p>
                    <br />
                  </p>
                  <p>
                    　　毓秀湖是兰大校园的人工湖，它的开挖同样是刘冰决策。“在老校长看来，有了水，就有了灵气。”刘易说，由于当时经费紧张，刘冰动员兰大师生自力更生挖掘人工湖，“学生每人每天有定量，以劳动生产课的形式加以考核”。
                  </p>
                  <p>
                    <br />
                  </p>
                  <p>
                    　　在刘冰主持兰大期间，兰州军区政委肖华、甘肃省委书记宋平曾亲自带领解放军指战员和省委机关干部，前往兰大校园打扫卫生、清洁校园。这极大地鼓励了全校师生整治校园环境的干劲。
